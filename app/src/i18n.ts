let activeLocale: Locale = "en";

export async function initLocale(saved: string): Promise<Locale> {
  if (saved !== "auto" && (saved as Locale) in TRANSLATIONS) {
    activeLocale = saved as Locale;
    return activeLocale;
  }
  // "auto" — derive from OS locale via tauri-plugin-os.
  try {
    const osLocale = await invoke<string | null>("plugin:os|locale");
    if (osLocale) {
      const short = osLocale.toLowerCase().split(/[-_]/)[0];
      if (short && (short as Locale) in TRANSLATIONS) {
        activeLocale = short as Locale;
        return activeLocale;
      }
    }
  } catch {
    // ignore — fall through to navigator.language
  }
  const nav = (navigator.language || "en").toLowerCase().split("-")[0];
  if ((nav as Locale) in TRANSLATIONS) {
    activeLocale = nav as Locale;
  }
  return activeLocale;
}

export function setLocale(loc: Locale) {
  activeLocale = loc;
}

export function currentLocale(): Locale {
  return activeLocale;
}

export function t(key: string): string {
  const dict = TRANSLATIONS[activeLocale];
  return dict[key] ?? TRANSLATIONS.en[key] ?? key;
}
